package Data;

public class Typer {
    private String Aftaletype;
    private String Varighed;
    private String Afdeling;

    public String getAftaletype() {
        return Aftaletype;
    }

    public void setAftaletype(String aftaletype) {
        this.Aftaletype = aftaletype;
    }

    public String getVarighed() {
        return Varighed;
    }

    public void setVarighed(String varighed) {
        Varighed = varighed;
    }

    public String getAfdeling() {
        return Afdeling;
    }

    public void setAfdeling(String afdeling) {
        Afdeling = afdeling;
    }
}
